// This program calculates and prints 1/3 using integer, double, and mixed aritmetic.
#include <iostream>
using namespace std;

int main() {
    cout << "1    /3   = " << 1/3 << endl;
    cout << "1.0  /3.0 = " << 1.0/3.0 << endl;
    cout << "1.0  /3   = " << 1.0/3 << endl;
    cout << "1    /3.0 = " << 1/3.0 << endl;
    return 0;
}